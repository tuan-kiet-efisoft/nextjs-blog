export async function getServerSideProps({ req, res }) {
  res.setHeader(
    'Cache-Control',
    'public, s-maxage=10, stale-while-revalidate=59'
  )
  const allPostsData = await getSortedPostsData()
  return {
    props: {
      allPostsData
    }
  }
}